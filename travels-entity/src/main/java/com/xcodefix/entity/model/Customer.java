package com.xcodefix.entity.model;

public class Customer {
	
	private long id;
	private String customerName;
	private String contactNo;
	private String alterNativeContactNo1;
	private String alterNativeContactNo2;
	private String address;
	private String state;
	private String city;
	private String pincode;
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getCustomerName() {
		return customerName;
	}
	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}
	public String getContactNo() {
		return contactNo;
	}
	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}
	public String getAlterNativeContactNo1() {
		return alterNativeContactNo1;
	}
	public void setAlterNativeContactNo1(String alterNativeContactNo1) {
		this.alterNativeContactNo1 = alterNativeContactNo1;
	}
	public String getAlterNativeContactNo2() {
		return alterNativeContactNo2;
	}
	public void setAlterNativeContactNo2(String alterNativeContactNo2) {
		this.alterNativeContactNo2 = alterNativeContactNo2;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getPincode() {
		return pincode;
	}
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	

}
